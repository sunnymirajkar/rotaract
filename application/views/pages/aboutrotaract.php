				<h3>ROTARACT HISTORY</h3>
				<p>
					Rotaract was founded forty years ago, with the Rotaract Club of North Charlotte,
					Carolina being the first club to be chartered - on March 13, 1968. However, as early as
					1935, Paul Harris encountered a youth organization based on Rotary principles in
					Australia. In the 1950's many youth clubs were sponsored by Rotary clubs under various
					names. These included the “Paul Harris Circles” in Europe and the “Rotors” clubs created
					in some American Universities. In 1965, these organizations experienced significant
					growth - in part due to the Interact program created by Rotary International in 1962.
					Many Interactors, having reached the age limit of that program, were starting new types
					of clubs called “Senior Interact”. The new program’s name, Rotaract, was created by
					Rotary International as a combination of the words “Rotary” and “Action”. The aim of the
					program was stated to be “to develop leadership and responsible citizenship through
					Rotaract Club of North Charlotte, many existing Rotary based youth clubs changed their
					names to become an official Rotaract club. In the 1990's, Rotaractors combined their
					organizations on an international level with the creation of Multi-District Organizations
					in Europe, Australia and South America. There are over 7,000 clubs in about 163
					countries and geographical areas. Internationally, Rotaract is strong in Asia, Africa and
					South America as well as Europe, where Rotary membership may be exclusive or inclusive of Rotaract membership.
